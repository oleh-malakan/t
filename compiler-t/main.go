package main

import (
	"flag"
	"io"
	"log"
	"os"
)

func main() {
	srcPath := flag.String("src", "../t.t", "")
	outPath := flag.String("out", "../t.elf", "")
	flag.Parse()
	srcFile, err := os.OpenFile(*srcPath, os.O_RDONLY, os.ModePerm)
	if err != nil {
		log.Fatal(err)
	}

	outFile, err := os.Create(*outPath)
	if err != nil {
		log.Fatal(err)
	}
	defer outFile.Close()
	
	if err := outFile.Chmod(os.FileMode(0111) | os.ModePerm); err != nil {
		log.Fatal(err)
	}

	var outOffset int64
	output := []byte{
		0x7F, 0x45, 0x4C, 0x46, 0x02, 0x01, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0x02, 0x00, 0xB7, 0x00, 0x01, 0x00, 0x00, 0x00, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
	}
	n, err := outFile.Write(output)
	if err != nil {
		log.Fatal(err)
	}
	outOffset += int64(n)

	b := make([]byte, 1024)
	var srcOffset int64
	for {
		n, err := srcFile.ReadAt(b, outOffset)
		if err != nil && err != io.EOF {
			log.Fatal(err)
		}

		if n == 0 {
			break
		}
		srcOffset += int64(n)
	}

}
